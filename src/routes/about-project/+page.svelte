    <span class="uptitle">O projektu</span>
    <h1>Aurionth</h1>
    <span class="subtitle">Fe pêrpetumo Lumôr – V neustálém světle</span>

    <h2 id="ap-what">Co to je?</h2>
    <p>Aurionth je můj osobní projekt, který se zaměřuje na tvorbu rozsáhlého fiktivního světa. Tento projekt zahrnuje nejen geografii či historii, ale také kultury, jazyky, mytologie a mnoho dalších aspektů. Mým cílem je vytvořit co nejkomplexnější a nejdetailnější svět, založený na reálných základech, který působí jako skutečný svět, který by mohl existovat vedle našeho.</p>
    <p>Projekt je stále ve vývoji a neustále se rozšiřuje o nové informace, příběhy i detaily. Je to pro mě způsob, jak se vyjádřit a zároveň vytvořit něco, co může inspirovat ostatní.</p>
    <p>Aurionth není o úniku. I když by se to nabízelo. Je o prozkoumávání reálných zákoutí, které si často ani neuvědomujeme. Je to o hledání diamantu v moři drahokamů. O vyjádření odlišnosti v rámci toho, co považujeme za normální.</p>

    <h2 id="ap-born">Jak to vzniklo?</h2>
    <p>Aurionth je jako lidstvo. Náhoda v hromadě <span class="chan">nepravděpodobností</span>. Vznikl z pouhé tvorby nějaké neznámé mapy a již chvíli vznikajícího děje v mé knize. Z náhodné mapy se stala mapa náhodného světa. Z náhodného světa se stal svět, který vidíme dnes.</p>
    <p>Vše se stalo náhodou, stejně jako lidstvo. A podobně jako ono se Aurionth neustále rozvíjí a mění, přičemž si ponechává svou primární a  podstatu. <i>Mužnost.</i></p>
    <p>Nepředstavujte si však <i>mužnost</i> jako něco známého. Zde je tento pojem pouze obrazem aurionthské reality.</p>

    <h2 id="ap-essence">Jaká je podstata?</h2>
    <p>Podstata Aurionthu je mužnost.</p>
    <p>Ano i ne. Je to svoboda, volnost, respekt, láska, síla, strach i odhodlání.</p>
    <p>Je to všechno. A zároveň nic. Nedá se to popsat jedním slovem. Dá se to jen a vnímat.</p>
    <p>Aurionth má svou pravou podstatu v pochopení jednotlivce – v jeho svobodné mysli a odhodlaném srdci. Je to o tom, zda se člověk dokáže postavit věcem, které nejsou „normální“ v pohledu ostatních.</p>
    <p>Zda se dokáže postavit předsudkům, strachu či , které nám společnost často klade na ramena jako něco, co nás má charakterizovat.</p>
    <p>Ale není to tak. Očekávání je něco, co můžeme přijmout a žít s tím, nebo kolem toho projít a nechat to být. </p>
    <p>A o tomhle je Aurionth. O tom, že zde můžeme být kýmkoli chceme být. Pokud tím neporušujeme zákony vesmíru.</p>

    <h2 id="ap-definition">A co to vlastně je?</h2>
    <p>Aurionth je „maskulinní“ svět, ale ne ve smyslu, jaký si asi myslíte. Není jen tvrdý, pevný a plný odvahy.</p>
    <p>Je to svět, kde je z našeho pozemského pohledu přítomno pouze mužské pohlaví. To však neznamená, že je to svět bez emocí, lásky, strachu či slabosti. Právě naopak.</p>
    <p>Aurionth je svět rovnováhy.</p>
    <p>Slabost a strach zde stojí vedle síly a odhodlání a ani jedno nepřevládá.</p>
    <p>Je to prostor, kde se evoluce vyvinula jinak. Aurionthští muži jsou schopni reprodukce jak pozemského typu, tak i formy, která zahrnuje porod. Tomu se však věnuje samostatný článek.</p>
    <p>Aurionth je svět plný síly, mužnosti a odvahy,ale zároveň jím prostupuje něha, láska, strach i bolest. Mužnost zde nabývá zcela jiného významu.</p>

    <h2 id="ap-findings">Co zde najdeme?</h2>
    <p>Krom již zmíněné mechaniky reprodukce, je zde pestrá škála informací. Najdeme zde geografické zápisy, jazykové struktury, kulturní zvyklosti, náboženská božstva, mytologické, historické, či třeba děsivé příběhy.</p>
    <p>Je toho tolik, že by se to nevešlo ani na tuto stránku.</p>

    <h2 id="ap-author">Kdo je tvůrce?</h2>
    <p>Mé jméno je Aurittinth Kîllis No’Uu, ale můžete mi říkat Aurittinth či zkráceně Auri. Toto jméno je pseudonym, který jsem si zvolil pro tento svět.</p>
    <p>Jsem člověk, který miluje kreativitu, umění a příběhy. Aurionth je způsob, jak tyto věci propojit v jedno a pracovat na něčem, co mě naplňuje.</p>
    <p>Představte si mě jako někoho, kdo objevil neprozkoumaný ostrov a nyní vám předává zprávy o jeho světě, obyvatelích a nepředstavitelné historii.</p>

    <h2 id="ap-finish">Kdy bude hotov?</h2>
    <p>Tvorba světa je náročnější než napsání knihy o několika stovkách stránek. Je to proces, který zahrnuje čas, prostor i kus života.</p>
    <p>A hlavně – je to proces, který možná nemá konec.</p>

